typealias ContactsListener = (contacts: List<Contact>) -> Unit

class PhoneBookDemoService: PhoneBookService {
    private var contacts = mutableListOf<Contact>()
    private var loaded = false
    private val listeners = mutableSetOf<ContactsListener>()

    override fun loadContacts(): Task<Unit> = SingleTask<Unit>(Callable {
        Thread.sleep(2000)
        val faker = Faker.instance()
        IMAGES.shuffle()
        contacts = (1..100).map { id ->
            val contact = Contact(
                id = id.toLong(),
                name = faker.name().name(),
                phoneNumber = faker.phoneNumber().phoneNumber(),
                avatar = IMAGES[id % IMAGES.size]
            )
            contact
        }.toMutableList()
        loaded = true
        notifyChanges()
    })

