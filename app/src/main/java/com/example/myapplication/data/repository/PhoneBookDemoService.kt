    override fun deleteContact(contact: Contact): Task<Unit> = SingleTask<Unit>(Callable {
        Thread.sleep(1000)
        val index = contacts.indexOfFirst {
            contact.id == it.id
        }
        if (index != -1){
            contacts = ArrayList(contacts)
            contacts.removeAt(index)
            notifyChanges()
        }
    })

    override fun addListener(listener: ContactsListener) {
        listeners.add(listener)
        if (loaded){
            listener.invoke(contacts)
        }
    }

    override fun removeListener(listener: ContactsListener) {
        listeners.remove(listener)
    }

    private fun notifyChanges(){
        if (!loaded) return
        listeners.forEach{
            it.invoke(contacts)
        }
    }

